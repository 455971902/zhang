typedef struct GROUP         // 定时模式
{
    TIME start_time;           // 启动时间
    unsigned int flow;         // 流量
    unsigned int smp_time;     // 采样时长
    unsigned char srb;         // 组开关标识,srb=1,表示改组"启用"，为0表示改组"未启用"	
}GROUP;

typedef struct CYL				//循环模式
{
    unsigned int mid_time;       // 间隔时间
    unsigned int smp_time;       // 采样时长
    unsigned int flow;           // 采样流量
    unsigned char cyl_num;        // 循环次数
}CYL;


typedef struct MANU				//手动模式
{
  unsigned int flow;           	// 采样流量
  unsigned int smp_time;       // 采样时长  min
  unsigned char temp_flag;		//温度选择标志
  int temperature;			//温度
}MANU;

typedef struct CLV				//定容模式
{
  double_x   smp_volume;          // 采样体积(l)
  unsigned int flow;           // 采样流量  
  unsigned char cyl_num;         // 循环次数
  unsigned int mid_time;       // 间隔时间min
}CLV;

typedef struct SYSTEM    //系统结构体
{
	unsigned char mod;            //工作模式,分HANDMOVE,TIMING,CYLCLE三种模式
	unsigned char old_mod;

	unsigned char status;         //工作状态，STOP表示停止，WORKING表示正在工作，按启动键将切换这两种状态
	unsigned char oldstatus;
	
	GROUP groups[GROUP_NUM];     //十个工作组
	CYL   cyls;                  // 循环采样结构体
	MANU  manu;                   //手动模式结构体
	CLV	  constant_v;				  //定容模式结构体
	unsigned char cur_group;      //定时模式下的当前工作组
	unsigned char set_group;	  // 设置的组号
	unsigned char err_group_num;  // 冲突组号
	unsigned long group_status;  //定时模式组的状态标志，每两位表示一个组，00表示就绪，01表示正在采样，10表示已结束

	unsigned char cur_cyls;      //循环模式下的当前循环到的次数
	//unsigned char cur_cyls_status; //循环模式下当前循环次数的状态，1表示就绪，2表示正在采样
	
	unsigned int start_min;      //定时时间（0~23）     //总采样时间为 used + sec;
	unsigned char start_sec;     //定时时间（0~59）

	unsigned int used_time;      //已采样时长（min）//时间等于s+min     //总采样时间为 used + sec;
	unsigned char used_sec;     //循环或者手动模式启动时的时钟秒，使时间精确到秒(s)
	
							   //1、手动模式下，used_time只要一按"启动"键就开始以分为单位计时，按停止键则回0
							   //2、循环模式下，used_time只要一按"启动"键就开始以分为单位计时
							   //   当used_time等于cyls.smp_time时，used_time回0，又经过cyls.mid_time分钟
							   //   下一次采样开始，used_time又开始计时，又直到等于cyls.smp_time再回0，直
							   //    到所有的循环都结束为止
							   //3、定时模式下，used_time从最近要发生采样的一个组的启动时间开始计时，计时到
							   //groups[cur_group].smp_time分钟，used_time回0,等第二个组的启动时间又开始计时

	unsigned int wait_time;      //距离下一次采样需等待的时间，wait_time分钟（间隔时间） 
	unsigned char wait_sec;       //等待的秒

	double  percent;             //完成的百分比(暂时只有定容存在)
							   //1、手动模式下，percent先暂时为0
							   //2、循环模式下，percent=used_time/cyls.smp_time
							   //3、定时模式下，percent=used_time/groups[cur_group].smp_time

	float dynamic_flow;   		//动态的流量，也即从电机的反馈电压估算出来的动态流量(ml)
    double dynamic_volume;  	// 动态实际体积(ml)
    double dynamic_cail_volume;  // 动态标况体积

	TIME  sys_time;              //系统时间
	TIME  power_time;            //定时关机时间
	
	unsigned char power_status;  //定时关机启用状态

	//unsigned char language;          // 语言，1为中文，0为英文
	//unsigned char set_language;		// 在设置语言时使用作临时值

	unsigned char temp_year;  			//用于调整用
	unsigned char temp_month;
	unsigned char temp_day;
	unsigned char temp_hour;              // 用于更新时间的"时"临时缓存  设置时间
	unsigned char temp_min;               // 用于更新时间的"分"临时缓存
	unsigned char temp_sec;               // 用于更新时间的"秒"的临时变量

	int a_hour;                           // 用于调整时间用的"时"临时缓存
	int a_min;
	int a_sec;

	int a_flow;                           //用于调整流量时的临时变量
	int a_temp;                           //用于调整温度或次数时的临时变量
	
	unsigned char	set_temp_flag;		  //用于观察温度是否设定	
	
    double a_volume;                       // 用于调整体积时的临时变量(l)
	
	unsigned int presure;                 //流量对应的压力值
    unsigned int bat_count;               // 电量低时用来计数
	
	float envir_temp;						//环境温度
	float bar_pre;							//环境气压
	
}SYSTEM;
