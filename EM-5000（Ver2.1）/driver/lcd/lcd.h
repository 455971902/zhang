#define W_ON     P4OUT&=~BIT6   //写信号有效
#define R_ON     P4OUT|=BIT6    //读信号有效

#define E_OFF    P4OUT&=~BIT7   //允许信号无效
#define E_ON     P4OUT|=BIT7    //允许信号有效

